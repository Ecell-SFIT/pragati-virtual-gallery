$(document).ready(function () {
  projectData.forEach((item, index) => {
    $("#menu-dropdown").append(group(item, index));
  });

  projectData.forEach((item, index) => {
    let roomId = `#list-${item.id}`;
    item.projects.forEach((item, index) => {
      $(roomId).append(getListItem(item, index));
    });
  });
});

group = (item, index) => {
  console.log(item);

  let collapse = false;
  if (index == 0) {
    collapse = true;
  }

  return `
    <div class="group">
            <div class="menu-header" id="${item.id}">
              <p
                class="collapse-button"
                type="button"
                data-toggle="collapse"
                data-target="#collapse-${item.id}"
                aria-expanded="${collapse}"
                aria-controls="collapse-${item.id}"
              >
                ${item.name}&nbsp;&nbsp;<i class="fa fa-chevron-down"></i>
              </p>
            </div>

            <div
              id="collapse-${item.id}"
              class="collapse ${collapse ? "show" : ""}"
              aria-labelledby="heading${index}"
              data-parent="#menu-dropdown"
            >
              <div class="card">
              <div class="card-body">
              <ul id="list-${item.id}" class="list-group">
              </ul>
              </div>
              </div>
            </div>
          </div>`;
};

getListItem = (item, index) => {
  return `<li class="list-group-item"><a href="${item.url}" target="_blank">${item.name}</a></li>`;
};
